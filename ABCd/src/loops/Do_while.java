package loops;

public class Do_while {
	
	public static void main(String[] args) {
		//print 1 to 3 no.
		
		int a=1;                                      // start condition
		do {System.out.println(a);                    // do { printing statement
		a++;}                                         // increment/ decrement }
		while(a<=3);                                  // while(end condition)
		}
}
