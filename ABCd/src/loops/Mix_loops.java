package loops;

public class Mix_loops {
	public static void main(String[] args) {
		
	//print 1 to 5 no. using for loop
	for(int a=1;a<=5;a++) {
		System.out.println(a);}
	
	System.out.println("~~~~~~~~~~~~~~~~~~");
	
	
	//print 1 to 5 no. using while loop
	int b=1;
	while(b<=5) {
		System.out.println(b);
		b++;}

	System.out.println("~~~~~~~~~~~~~~~~~~~~");
	
	//print 1 to 5 no. using do while loop
	int a=1;
	do {System.out.println(a);
	a++;}
	while(a<=5);}
	}

