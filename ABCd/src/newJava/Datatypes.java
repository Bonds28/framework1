package newJava;

public class Datatypes {
	public static void main(String[] args) {
		
		//Primitive data types
		//(numeric + non decimal)
		
		//1.byte
		byte b=80;
		byte b1=127;                   //max limit
		//byte b3=128;
		System.out.println(b);
		System.out.println(b1);
		//System.out.println(b3);
		
		
		//2.short
		short s=32767;                  //max limit
		//short s1=32768;
		System.out.println(s);
		
		
		//3.int
		int i=2147483647;                //max limit
		//int i1=2147483648;
		System.out.println(i);
		
		
		//long
		long l=21474836476644l;           //max limit
		//long l=21474836473444l;
		System.out.println(l);
		
		
		//(numeric + decimal)
		//1.float
		float f=36.65f;
		float f1=96.5f;
		System.out.println(f);
		System.out.println(f1);
		
		
		//2.double
		double d= 48.55;
		double d2=78.23d;
		System.out.println(d);
		System.out.println(d2);
		
		
		//Character
		char c='H';
		System.out.println(c);
		
		
		//conditional
		//boolean
		boolean b3=true;
		boolean b5=false;
		System.out.println(b3);
		System.out.println(b5);
	}

}
